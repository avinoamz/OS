package os1;

public class Data {

    private int y, z;

    public Data() {
        y = 0;
        z = 0;
    }

    public Data(int y, int z) {
        this.y = y;
        this.z = z;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

}
